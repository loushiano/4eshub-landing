              <h2 id="faq">Frequently asked questions about customer feedback and ISO 9001</h2>

              <h3>Does ISO 9001 require customer satisfaction surveys?</h3>
              <p>
                No. ISO 9001 Clause 9.1.2 requires organizations to monitor customer
                perceptions and to define their own methods for obtaining, monitoring,
                and reviewing that information. Customer satisfaction surveys are one
                acceptable method among others, such as complaints analysis, customer
                meetings, warranty data, and market-share trends.
              </p>

              <h3>What is the difference between customer feedback and Voice of the Customer?</h3>
              <p>
                Customer feedback is any input about satisfaction, expectations, or
                experience. Voice of the Customer (VoC) is a structured program that
                systematically collects, prioritizes, analyzes, and acts on that
                feedback—often with defined ownership, review cadence, and closed-loop
                follow-up—so insights drive QMS improvement and management review.
              </p>

              <h3>How should customer satisfaction data feed management review?</h3>
              <p>
                ISO 9001 Clause 9.3 (Management review) expects customer satisfaction
                among review inputs. Organizations should present analyzed trends—not
                raw scores—including themes, corrective actions taken, open
                improvement opportunities, and whether customer-facing teams closed
                the loop when changes were made.
              </p>

