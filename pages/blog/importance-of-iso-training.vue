              <h2>Frequently asked questions about ISO training</h2>

              <h3>Is ISO training mandatory for certification?</h3>
              <p>
                ISO standards do not prescribe a fixed curriculum, but they
                require organizations to ensure competence for roles that affect
                the management system and to retain evidence. In practice,
                certification bodies expect documented training programs,
                role-based requirements, and proof that people were trained on
                current procedures—especially for processes sampled during audits.
              </p>

              <h3>How often should ISO training be refreshed?</h3>
              <p>
                Refresh frequency depends on risk, role, regulatory change, and
                procedure updates—not a single calendar rule. Many organizations
                set annual awareness refreshers, require immediate retraining when
                controlled documents change, and schedule role-specific refreshers
                based on competency matrices or certification expiry dates.
              </p>

              <h3>What is the difference between awareness training and competence training?</h3>
              <p>
                Awareness training gives broad understanding of the management
                system, policies, and individual responsibilities. Competence
                training is role-specific: it verifies that a person can perform
                defined tasks correctly, often with evaluation or authorization
                before they work independently. Both are required; they serve
                different purposes in an audit sample.
              </p>

