              </p>

              <h3>Awareness training</h3>
              <p>
                Broad, organization-wide training that explains why the management
                system exists, which standards apply, and how each employee
                contributes. This is often the first step after gap assessment
                when pursuing certification and should be refreshed when scope or
                policy changes materially.
              </p>

              <h3>Role-based and job-specific training</h3>
              <p>
                Training tied to defined roles, competencies, and organization
                units. Operators, supervisors, and specialists receive instruction
                on the procedures they execute—including safety-critical tasks,
                environmental controls, or data-handling rules under ISO 27001.
              </p>

              <h3>Procedure-change and retraining</h3>
              <p>
                When a controlled document is revised, affected personnel must be
                notified and retrained. This is one of the most frequently missed
                requirements in growing organizations. Linking document approval
                workflows to training assignments prevents "everyone still follows
                the old version" scenarios.
              </p>

              <h3>Internal auditor training</h3>
              <p>
                Internal audits are how organizations "check" their management
                system. Auditor training covers planning, interviewing, evidence
                collection, reporting findings, and follow-up. Competent internal
                auditors improve surveillance readiness and surface improvement
                opportunities year-round.
              </p>

              <h3>Effectiveness evaluation</h3>
              <p>
                Training is not complete when attendance is logged. Standards expect
                organizations to evaluate whether training achieved its intended
                outcomes—through tests, practical assessment, supervision, or
                post-training performance review. That evaluation becomes part of
                the competence record auditors review.
              </p>

