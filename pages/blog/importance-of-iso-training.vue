              <h2>What is ISO training?</h2>
              <p>
                ISO training is a structured program that educates employees,
                managers, and auditors about the requirements, principles, and
                practices of one or more ISO management system standards. It is
                not a one-time onboarding session. It is an ongoing competence
                activity that covers awareness, role-specific skills, procedure
                changes, and—where needed—formal auditor qualification.
              </p>

              <p>
                Common standards addressed through ISO training include:
              </p>
              <ul class="list-disc pl-6 space-y-2 mb-6 text-gray-600">
                <li>
                  <strong>ISO 9001</strong> — Quality Management Systems (QMS)
                </li>
